function setLanguage(lang) {
    const elements = document.querySelectorAll(".translate");
    
    elements.forEach(element => {
        const key = element.getAttribute("data-translate");
        if (translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    const webButton = document.querySelectorAll(".web-button, .projectWebsiteImageTitle");
    const githubButton = document.querySelectorAll(".github-button, .projectGithubImageTitle");
    const githubPagesButton = document.querySelectorAll(".github-pages-button");
    
    const buttonHomeAboutTitle = document.querySelectorAll(".homeButtonAbout");
    const name = document.querySelectorAll(".name");

    const coolItemsConsole = document.querySelectorAll(".coolItemsConsole");
    const coolItemsCard = document.querySelectorAll(".coolItemsCard");
    const coolItemsChatbot = document.querySelectorAll(".coolItemsChatbot");

    const chatbotTextarea = document.getElementById("user-input");

    webButton.forEach(button => {
        button.title = translations[lang].webButtonTitle; // Ustawienie tytułu dla każdego divu
    });
    githubButton.forEach(button => {
        button.title = translations[lang].githubButtonTitle; // Ustawienie tytułu dla każdego divu
    });
    githubPagesButton.forEach(button => {
        button.title = translations[lang].githubPagesButtonTitle; // Ustawienie tytułu dla każdego divu
    });

    buttonHomeAboutTitle.forEach(button => {
        button.title = translations[lang].buttonHomeAboutTitle; // Ustawienie tytułu dla każdego divu
    });
    name.forEach(p => {
        p.title = translations[lang].nameTitle; // Ustawienie tytułu dla każdego divu
    });

    coolItemsConsole.forEach(Image => {
        Image.title = translations[lang].coolItemsConsole; // Ustawienie tytułu dla każdego divu
    });
    coolItemsCard.forEach(Image => {
        Image.title = translations[lang].coolItemsCard; // Ustawienie tytułu dla każdego divu
    });
    coolItemsChatbot.forEach(Image => {
        Image.title = translations[lang].coolItemsChatbot; // Ustawienie tytułu dla każdego divu
    });
    
    if (chatbotTextarea) {
        if (translations[lang].chatbotTextarea) {
            chatbotTextarea.placeholder = translations[lang].chatbotTextarea;
        }
    }

    const cvLinks = document.querySelectorAll('.cv-download a');

    cvLinks.forEach(link => {
        if (lang === 'en') {
            link.href = 'downloads/en_Wiktor Gołąb CV.pdf';
            link.title = 'en_Wiktor Gołąb CV.pdf';
        } else if (lang === 'pl') {
            link.href = 'downloads/pl_Wiktor Gołąb CV.pdf';
            link.title = 'pl_Wiktor Gołąb CV.pdf';
        }
    });
    
    localStorage.setItem("language", lang);
}

function updateTranslateAttribute() {
    const element = document.getElementById('projectsWindowTitle');
    if (element) {
        const language = localStorage.getItem("language") || "pl";
        const screenWidth = window.innerWidth;
        const translateKey = screenWidth <= 560 ? 'projectsWindowTitleShort' : 'projectsWindowTitle';

        // Ustawiamy atrybut data-translate
        element.setAttribute('data-translate', translateKey);

        // Ustawiamy tekst na podstawie tłumaczeń
        element.textContent = translations[language][translateKey];
    }
}

